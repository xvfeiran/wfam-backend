CREATE TABLE APMS_PART (
  ID                      VARCHAR2(36 CHAR)  PRIMARY KEY,
  PART_NUMBER             VARCHAR2(30 CHAR)  UNIQUE,
  ORDER_ID                VARCHAR2(36 CHAR)  NOT NULL REFERENCES APMS_RETURN_ORDER(ID),
  PART_CODE               VARCHAR2(50 CHAR)  NOT NULL,
  BUSINESS_UNIT           VARCHAR2(50 CHAR)  NOT NULL,
  PRODUCT_PLATFORM        VARCHAR2(50 CHAR)  NOT NULL,
  PRODUCTION_SHIFT        VARCHAR2(20 CHAR),
  COMPLAINT_TYPE          VARCHAR2(20 CHAR),
  VEHICLE_PRODUCTION_DATE DATE,
  VEHICLE_PURCHASE_DATE   DATE,
  VEHICLE_FAILURE_DATE    DATE,
  VEHICLE_VIN             VARCHAR2(17 CHAR),
  VEHICLE_MILEAGE         NUMBER(10),
  CUSTOMER_DESCRIPTION    VARCHAR2(500 CHAR),
  OTHER_DESCRIPTION       VARCHAR2(500 CHAR),
  REPAIR_STATION          VARCHAR2(100 CHAR),
  COMPLAINT_LOCATION      VARCHAR2(100 CHAR),
  RESPONSIBLE_ENGINEER    VARCHAR2(100 CHAR),
  ANALYST                 VARCHAR2(100 CHAR),
  QC_NO                   VARCHAR2(50 CHAR),
  IS_SAMPLE               NUMBER(1)          DEFAULT 0,
  STATUS_CHANGED_AT       TIMESTAMP,
  STATUS                  VARCHAR2(50 CHAR)  NOT NULL,
  CREATED_BY              VARCHAR2(100 CHAR) NOT NULL,
  CREATED_AT              TIMESTAMP          NOT NULL,
  UPDATED_BY              VARCHAR2(100 CHAR),
  UPDATED_AT              TIMESTAMP
);
